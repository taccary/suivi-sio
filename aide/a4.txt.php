Sauvegarde#

<p>Ce menu permet de sauvegarder volontairement le contenu complet de la base 
  de donn&eacute;es sur le serveur web.</p>
<h4>Sauvegarde base</h4>
<p>Cette fonction vous permet de forcer la sauvegarde de l'int&eacute;gralit&eacute; 
  de la base de donn&eacute;es : cette fonction n'est &agrave; utiliser que si 
  l'on juge &agrave; un instant donn&eacute; la situation critique. En effet un 
  processus r&eacute;alise automatiquement en arri&egrave;re-plan une sauvegarde 
  journali&egrave;re de la base, ce qui devrait &ecirc;tre suffisant dans pratiquement 
  tous les cas de figure. Exemple de situation critique : on d&eacute;cide une 
  r&eacute;installation du serveur alors que des &eacute;tudiants viennent de 
  saisir de nombreuses donn&eacute;es.</p>
<p>L'effondrement d'un serveur h&eacute;bergeant un service web et un service 
  sql met en difficult&eacute; cette solution : une copie r&eacute;guli&egrave;re 
  du fichier sur une machine diff&eacute;rente est donc recommand&eacute;e.</p>
