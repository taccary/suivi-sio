Situations#
<p>Ce menu permet de g&eacute;rer les situations de travail que vous souhaitez
  faire apparaitre dans votre tableau de synth&egrave;se.</p>
<h4>Nouvelle situation</h4>
<p>Les onglets &agrave; votre disposition permettent de saisir des informations
  caract&eacute;ristiques de la situation professionnelle. Les champs dont la
  pr&eacute;sence est obligatoire sont marqu&eacute;s par une ast&eacute;risque.</p>
  <p>Le passage d'un onglet &agrave; l'autre g&eacute;n&egrave;re un enregistrement des donn&eacute;es saisies ou modifi&eacute;es.
  L'interpr&eacute;teur javascript doit &ecirc;tre activ&eacute;.</p>
<p>La longueur des donn&eacute;es saisies d&eacute;pend de la zone concern&eacute;e
  et est limit&eacute;e par le champ de la base de donn&eacute;es. Le libell&eacute;
   court est limit&eacute; &agrave; 64 caract&egrave;res, les autres champs &agrave; 255 caract&egrave;res.</p>

<p>Le libell&eacute; court sert d'identifiant significatif afin de faciliter la recherche ult&eacute;rieure d'une
situation en vue de la modifier ou de la compl&eacute;ter. Il sert aussi &agrave; caract&eacute;riser une
situation dans le tableau de synth&egrave;se.
<p>Dans les champs multilignes (Description, Lieu) le nombre de caract&egrave;re
  est limit&eacute; par la base de donn&eacute;es et non au niveau de la saisie.
  Les longueurs excessives seront donc tron&ccedil;onn&eacute;es.</p>

<p>L'enregistrement des donn&eacute;es de chacun des onglets se fait en utilisant
  le bouton &quot;Enregistrer&quot; pr&eacute;sent en bas chaque page ou en changeant d'onglet.</p>
<h4>Gestion situations</h4>
<p>Cet item permet de modifier une situation en cliquant sur son libell&eacute; court,
  ou de la supprimer en cliquant sur <i>supprimer</i> (pas d&eacute;finitivement
  &agrave; ce stade). L'interface indique si une situation est d&eacute;j&agrave;
  valid&eacute;e. Un situation supprim&eacute;e peut &ecirc;tre r&eacute;cup&eacute;r&eacute;e
  (retour &agrave; l'&eacute;tat initial) ou d&eacute;truite (destruction d&eacute;finitive).</p>
