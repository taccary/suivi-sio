<h3>Chaque titre d'un sous-menu h&eacute;berge un lien vers une rubrique d'aide contextuelle</h3>
  <h2>Objectif</h2>
  <p>Cet espace de travail est destin&eacute; au suivi individualis&eacute; de l'acquisition
  des comp&eacute;tences professionnelles du Brevet de Technicien Sup&eacute;rieur SIO.</p>
  <h2>Fonctionnement</h2>
  <p>L'&eacute;tudiante ou l&#146;&eacute;tudiant saisit les informations caract&eacute;risant
    ses situations professionnelles puis s&eacute;lectionne les activit&eacute;s
    qu&#146;il a mobilis&eacute;es. Pour chaque activit&eacute; cit&eacute;e il
    pr&eacute;cise, reformule et apporte sa vision personnelle des comp&eacute;tences
    qu'il pense avoir d&eacute;velopp&eacute;es. Il compl&egrave;te cette descrition
    en fournissant un lien vers les documents ressources qui ont &eacute;t&eacute;
    utilis&eacute;s ou &eacute;labor&eacute;s.</p>
    <p>La professeure ou le professeur appr&eacute;cie la description des situations
    en apportant des commentaires.</p>
  <p> &Agrave; tout moment mais surtout lorsque l&#146;ensemble des situations
    est correctement d&eacute;fini, l'&eacute;tudiante ou l&#146;&eacute;tudiant
    peut g&eacute;n&eacute;rer un tableau de synth&egrave;se pr&eacute;sentant
    l&#146;ensemble des situations enregistr&eacute;es. Ce tableau de synth&egrave;se
    prend en compte le parcours de l'&eacute;tudiant au niveau de ses activit&eacute;s
    sp&eacute;cifiques : SISR, SLAM ou indiff&eacute;renci&eacute; pour le premier
    semestre par exemple. Le num&eacute;ro de candidat figurant en en-t&ecirc;te
    sera celui de la convocation &agrave; l'examen et s'initialise dans les &quot;param&egrave;tres&quot;.<br>
  </p>
  <h2>Authentification</h2>
  <p>Fournis par l'administrateur ou envoy&eacute;s par messagerie, chaque utilisateur
    a d&ucirc; recevoir son identifiant <i>(login)</i> et son mot de passe <i>(code).</i></p>
  <p>Le professeur saisit son adresse m&eacute;l (<i>login</i>) et son mot de
    passe (<i>code</i>) sensible &agrave; la casse. Son menu lui permet ensuite d'exploiter les donn&eacute;es
    d'une promotion choisie ; par défaut il consulte la premi&egrave;re promotion &agrave;
     laquelle il est affect&eacute;.</p>

  <p>L'&eacute;tudiant saisit son adresse m&eacute;l (<i>login</i>) et son mot
    de passe (<i>code</i>) sensible &agrave; la casse. Sa promotion est d&eacute;termin&eacute;e automatiquement.</p>
