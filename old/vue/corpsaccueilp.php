<h3>Chaque titre d'un sous-menu h&eacute;berge un lien vers une rubrique d'aide contextuelle</h3>
  <h2>Promotion</h2>
   
  <p>Les promotions que vous suivez apparaissent dans la liste d&eacute;roulante. 
    Le choix de l'une d'entre elles vous permet de consulter les travaux de tous 
    les &eacute;tudiants concern&eacute;s.</p>
  <h2>Suivi</h2>
  <p>Vous pouvez consulter puis commenter les situations enregistr&eacute;es par
    les &eacute;tudiants de votre promotion.</p>
  <p>Selon leur emplacement, r&eacute;seau local par exemple, les ressources dont 
    l'adresse est indiqu&eacute;e par l'&eacute;tudiant ne sont pas toujours accessibles 
    pour le professeur.</p>
  <h2>Donn&eacute;es</h2>
  <p>Vous pouvez observer le tableau de synth&egrave;se d'un &eacute;tudiant s&eacute;lectionn&eacute; 
    ou son tableau bilan.</p>
  <h2>Divers</h2>
  <p>Vous pouvez restaurer l'ensemble des donn&eacute;es (situations, commentaires, 
    etc.) sauvegard&eacute;es par un &eacute;tudiant, par exemple apr&egrave;s 
    un effacement accidentel. Il appartient &agrave; chaque &eacute;tudiant de 
    r&eacute;aliser cette sauvegarde et il doit vous fournir le fichier correspondant. 
    La sauvegarde n'est pas incr&eacute;mentielle et reconstitue les donn&eacute;es 
    de l'&eacute;tudiant telles qu'&agrave; la date de la sauvegarde. Cette fonction 
    est aussi accessible &agrave; l'administrateur de l'application.</p>
  <p>Vous pouvez aussi modifier les caract&eacute;ristiques de votre compte, votre
    mot de passe par exemple.</p>
